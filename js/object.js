// オブジェクトの宣言と値の代入を行う
const personalDate = { name:'侍太郎', age:36, gender:'男性' };

// オブジェクトの値を出力する
console.log(personalDate);

// 'age'というキーの値を更新する
personalDate.age = 37;

// 新しくプロパティ（キーと値）を追加する
personalDate.adress = '東京都';

// オブジェクトの値を出力する
console.log(personalDate);

// 'gender'というキーの値を出力する
console.log(personalDate.gender);